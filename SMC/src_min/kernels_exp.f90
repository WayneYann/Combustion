!EXPAND                     dxinv(3) * first_deriv_8( FY(i,j,k-4:k+4,n) )
                rhs(i,j,k,iryn) = rhs(i,j,k,iryn) + dxinv(3) * &
                   ( D8(1)*(FY(i,j,k+1,n)-FY(i,j,k-1,n)) &
                   + D8(2)*(FY(i,j,k+2,n)-FY(i,j,k-2,n)) &
